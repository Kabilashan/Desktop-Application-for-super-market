    private void jButton22ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton22ActionPerformed
        // TODO add your handling code here:
        Date d1 = null;
        Date d2 = null;
        String timeOut = txt_in.getText();
        String timeIn = txt_out.getText();

        SimpleDateFormat format = new SimpleDateFormat("hh:mm");

        try {

            d1 = format.parse(timeOut);
            d2= format.parse(timeIn);
        }
        catch (Exception e){
            e.printStackTrace();
        }

        long diff = d2.getTime() - d1.getTime();
        long diffMinutes = diff / (60 * 1000);
        long diffHours = diff / (60 * 60 * 1000);
        lbl_ot.setText(String.valueOf(diffHours));
        if(diffHours >= 8){
            int ot = (int) (diffHours - 8);
            lbl_otMonth.setText(String.valueOf(ot));
        }
        else{
            int ot=0;
            lbl_otMonth.setText(String.valueOf(ot));
        }
    }//GEN-LAST:event_jButton22ActionPerformed

