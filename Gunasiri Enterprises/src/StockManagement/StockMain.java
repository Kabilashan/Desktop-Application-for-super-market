        //SimpleDateFormat sdf = new SimpleDateFormat("yyyy-mm-dd",Locale.getDefault());
        int x=JOptionPane.showConfirmDialog(null, "Are you sure you want to add record?","Add record",JOptionPane.YES_NO_OPTION);
        if(x==0){
            String Productname = Namebox.getText();
            String supplier = supplierbox.getText();
            String description = desbox.getText();
            String category = categorybox.getSelectedItem().toString();
            Double price = Double.parseDouble(pricebox.getText());
            String expiredate =dateFormat.format (expirebox.getDate());
            int quantity = Integer.parseInt(qtybox.getSelectedItem().toString());
            String mfd = dateFormat.format(mdfbox.getDate());
            

            if(Productname.matches("[A-Z a-z]+")){

                if(supplier.matches("[A-Z a-z]+")){

                    if(!description.isEmpty()){

                        try{
                            String sql="insert into product"+"(productname,supplier,category,price,quntity,mfd,expiredate,description)"
                            + "values(?,?,?,?,?,?,?,?)";

                            pst=conn.prepareStatement(sql);
                            pst.setString(1 , Productname);
                            pst.setString(2 , supplier);
                            pst.setString(3, category);
                            pst.setDouble(4,price);
                            pst.setInt(5,quantity);
                            pst.setString(6,expiredate);
                            pst.setString(7,mfd);
                            
                            pst.setString(8,desbox.getText());

                            pst.execute();
                            JOptionPane.showMessageDialog(null, "Data is saved Successfully");
                            clear();
                            
                        }
                        catch(Exception e){
                            JOptionPane.showMessageDialog(null, e);
                        }

                    }

                    else{
                        JOptionPane.showMessageDialog(null,"Please Enter storing description !");
                        desbox.setText(null);}
                }

                else{
                    JOptionPane.showMessageDialog(null,"Invalid supplier name !");
                    supplierbox.setText(null);}

            }
            else{
                JOptionPane.showMessageDialog(null,"Invalid product Name !");
                Namebox.setText(null);

            }

        }
        
        tableLoad();

